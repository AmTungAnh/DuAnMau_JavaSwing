    private void tblChatLieuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblChatLieuMouseClicked
        int index = this.tblChatLieu.getSelectedRow();
        ChatLieu chatLieu = chatLieuList.get(index);
        if (chatLieu.getTrangThai().equals("Hoạt động")) {
            this.btnXoaChatLieu.setText("Ngừng hoạt động");
        } else if (chatLieu.getTrangThai().equals("Ngừng hoạt động")) {
            this.btnXoaChatLieu.setText("Kích hoạt");
        }
        this.txtMaChatLieu.setText(chatLieu.getMa());
        this.txtTenChatLieu.setText(chatLieu.getTen());
    }//GEN-LAST:event_tblChatLieuMouseClicked


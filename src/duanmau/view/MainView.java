    private void loadTableChatLieu() {
        chatLieuList = this.chatLieuService.getAllChatLieu("chat_lieu");
        int stt = 1;
        defaultTableModel = new DefaultTableModel();
        defaultTableModel.addColumn("STT");
        defaultTableModel.addColumn("Mã");
        defaultTableModel.addColumn("Tên");
        defaultTableModel.addColumn("Trạng thái");

        for (ChatLieu loadChatLieu : chatLieuList) {
            Object[] row = {stt, loadChatLieu.getMa(), loadChatLieu.getTen(), loadChatLieu.getTrangThai()};
            defaultTableModel.addRow(row);
            stt++;
        }
        System.out.println("lại");
    this.tblChatLieu.setModel(defaultTableModel);
    }
    
